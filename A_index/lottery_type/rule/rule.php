                    <h2 class="iconl"><span>特码</span></h2>
                      <ul>
                         <li>香港六合彩公司当期开出的最后一码为特别号或特码。假如投注号码为开奖号码之特码，视为中奖，其余情形视为不中奖。<br>
                         特码大小、单双、色波、生肖等玩法对应参考以上“号码盘势规则”的说明。</li>
                      </ul>
                </li>
                <li>
                    <h2 class="iconl"><span>正码</span></h2>
                      <ul>
                         <li>香港六合彩公司当期开出之前6个号码叫正码。每一个号码为一投注组合，假如投注号码为开奖号码之正码，视为中奖，其余情形视为不中奖。<br>
                         正码总和单双、大小玩法对应参考以上“号码盘势规则”的说明。</li>
                      </ul>
                </li>
                <li>
                    <h2 class="iconl"><span>正码特</span></h2>
                      <ul>
                         <li>正码特是指正1特、正2特、正3特、正4特、正5特、正6特：香港六合彩公司当期开出之前6个号码叫正码。第一时间出来的叫正1特，依次为正2特、正3特…… 正6特(并不以号码大小排序)。其下注的正码特号与现场摇珠开出之正码其开奖顺序及开奖号码相同，视为中奖， 如现场摇珠第一个正码开奖为49号，下注第一个正码特为49则视为中奖，其它号码视为不中奖。</li>
                      </ul>
                </li>
                <li>
                    <h2 class="iconl"><span>正码1-6</span></h2>
                      <ul>
                         <li>香港六合彩公司当期开出之前6个号码叫正码。第一时间出来的叫正码1，依次为正码2、正码3…… 正码6(并不以号码大小排序)。<br>
                         正码1,2...6的大小、单双等玩法对应参考以上“号码盘势规则”的说明。</li>
                     </ul>
                </li>
           	</ul>
